//10. looping can execute a block of code a number of times
//9. for, while, do while syntax
//syntax for loop
for (i = 0; i <= 5; i++) {
  console.log(i);
}
//syntax while
var a = 3;
while (a <= 5) {
  console.log(a);
  a++;
}
//syntax do while
var count = 1;
do {
  console.log(i);
  count++;
} while (count <= 10);
